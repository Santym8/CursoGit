
def suma(n1, n2):
    return str(n1 + n2)

def resta(n1, n2):
    return str(n1 - n2)

def multiplicacion(n1, n2):
    return n1 * n2

def division(n1, n2):
    return n1/n2

def potencia(n1, ex):
    return str(n1**ex)


print(suma(20,5))
print(resta(55,1))
print(multiplicacion(9,5))
print(division(25,5))
print(potencia(6,2))
    

