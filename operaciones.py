
def suma(n1, n2):
    return str(n1 + n2)

def resta(n1, n2):
    return str(n1 - n2)

def multiplicacion(n1, n2):
    return n1 * n2

def division(n1, n2):
    return n1/n2

def potencia(n1, ex):
    return str(n1**ex)


print(suma(2,3))
print(resta(5,1))
print(multiplicacion(5,5))
print(division(9,3))
print(potencia(2,3))
    

